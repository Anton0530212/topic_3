# Программа, которая запрашивает у пользователя
# два числа и выводит на экран их среднее арифметическое.
num_1 = float(input('Введите первое число: '))
num_2 = float(input('Введите второе число: '))

average = (num_1 + num_2) / 2
print('Среднее арифметическое чисел', num_1, 'и', num_2, 'равно', average)
